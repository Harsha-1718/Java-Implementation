package oopss;

public class encap {
	private String Name;
	private int id;
	private long mobno;
	
	public String getName() {
		return Name;
	}
	
	public void setName(String Name) {
		this.Name=Name;
	}
	
	public int getid() {
		return id;
	}
	
	public void setid(int id) {
		this.id=id;
	}
	
	public long getmobno() {
		return mobno;
	}
	
	public void setmobno(long mobno) {
		this.mobno=mobno;
	}
}
